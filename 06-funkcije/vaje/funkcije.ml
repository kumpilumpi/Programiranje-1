let fold_left_no_acc f sez  =
  let rec aux acc f = function 
    | [] -> acc
    | x :: xs -> aux (f acc x) f xs
  in 
    if List.length(sez) < 2 then failwith ("Prekratko") 
    else
    aux "" f sez

