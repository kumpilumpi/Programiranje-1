 # remove 1 [1; 1; 2; 3; 1; 2; 3; 1; 1];;
 - : int list = [2; 3; 2; 3]
[*----------------------------------------------------------------------------*)

let rec remove x = function
  | glava :: rep -> if glava = x then remove x rep else [glava] @ remove x rep
  | [] -> []
                                      
